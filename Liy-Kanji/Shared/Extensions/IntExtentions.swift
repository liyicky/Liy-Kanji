import Foundation

extension Int16 {
    func toInt() -> Int {
        return Int(self)
    }
}

extension Int32 {
    func toInt() -> Int {
        return Int(self)
    }
}

extension Int64 {
    func toInt() -> Int {
        return Int(self)
    }
}

extension Int {
    func toInt16() -> Int16 {
        return Int16(self)
    }
    
    func toInt32() -> Int32 {
        return Int32(self)
    }
    
    func toInt64() -> Int64 {
        return Int64(self)
    }
}
